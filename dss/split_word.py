jieba_cut = None

def init_jieba():
    # Because import jieba will execute many code, 
    # so this function make jieba just import one time.
    global jieba_cut
    
    import jieba

    jieba_cut = jieba.cut

def split_word(string, full_mode=False):
    global jieba_cut

    return jieba_cut(string, full_mode)
